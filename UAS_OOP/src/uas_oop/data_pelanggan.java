    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        String nama = txNama.getText();
        String telepon = txTelp.getText();
        String alamat = txAlamat.getText();
        
        try {
        Connection conn = dbkoneksi.koneksi();
        String sql = "INSERT INTO pelanggan (nama, telepon, alamat) VALUES (?, ?, ?)";
        PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, nama);
        pst.setString(2, telepon);
        pst.setString(3, alamat);
        pst.executeUpdate();

        JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan.");
        tampilkanData(); 
        resetForm();
    }catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    } 
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int baris = pelanggan.getSelectedRow(); 
        if (baris == -1) {
        JOptionPane.showMessageDialog(this, "Pilih data dulu di tabel!");
        return;
    }
    String id_pelanggan = pelanggan.getValueAt(baris, 0).toString(); 
    String nama = txNama.getText();
    String telepon = txTelp.getText();
    String alamat = txAlamat.getText();
    
    try {
        Connection conn = dbkoneksi.koneksi();
        String sql = "UPDATE pelanggan SET nama=?, telepon=?, alamat=? WHERE id_pelanggan=?";
        PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, nama);
        pst.setString(2, telepon);
        pst.setString(3, alamat);
        pst.setString(4, id_pelanggan);
        pst.executeUpdate();

        JOptionPane.showMessageDialog(this, "Data berhasil diupdate.");
        tampilkanData();
        resetForm();
    }catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        int baris = pelanggan.getSelectedRow(); 
        if (baris == -1) {
        JOptionPane.showMessageDialog(this, "Pilih data dulu di tabel!");
        return;
    }
    String id = pelanggan.getValueAt(baris, 0).toString();
    int konfirmasi = JOptionPane.showConfirmDialog(this, "Yakin ingin menghapus data ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);    
    
    if (konfirmasi == JOptionPane.YES_OPTION) {
        try {
            Connection conn = dbkoneksi.koneksi();
            String sql = "DELETE FROM pelanggan WHERE id_pelanggan=?";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, id);
            pst.executeUpdate();

            JOptionPane.showMessageDialog(this, "Data berhasil dihapus.");
            tampilkanData();
            resetForm();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
      }
    }//GEN-LAST:event_btnHapusActionPerformed

