    private void cariPelanggan() {
    DefaultTableModel model = (DefaultTableModel) pelanggan.getModel();
    model.setRowCount(0); 

    String keyword = txCariPelanggan.getText();

    try {
        Connection con = dbkoneksi.koneksi();
        String sql = "SELECT * FROM pelanggan WHERE nama LIKE ?";
        PreparedStatement ps = con.prepareStatement(sql);
        ps.setString(1, "%" + keyword + "%");
        ResultSet rs = ps.executeQuery();

        while (rs.next()) {
            Object[] row = {
                rs.getInt("id_pelanggan"),
                rs.getString("nama"),
                rs.getString("alamat"),
                rs.getString("telepon")
            };
            model.addRow(row);
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Gagal mencari: " + e.getMessage());
    }
}

    private void resetForm() {
        txNama.setText("");
        txTelp.setText("");
        txAlamat.setText("");
        modeTambah();
        pelanggan.clearSelection();
    }
    
    private void tampilkanData() {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("ID");
    model.addColumn("Nama");
    model.addColumn("No Telp");
    model.addColumn("Alamat");

    try {
        Connection conn = dbkoneksi.koneksi();
        String sql = "SELECT * FROM pelanggan";
        PreparedStatement pst = conn.prepareStatement(sql);
        ResultSet rs = pst.executeQuery();

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getString("id_pelanggan"),
                rs.getString("nama"),
                rs.getString("telepon"),
                rs.getString("alamat")
            });
        }

        pelanggan.setModel(model); 
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal menampilkan data: " + e.getMessage());
    }
    }
    
